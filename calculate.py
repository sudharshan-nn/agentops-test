def add_numbers(a, b):
    """
    Add two numbers and return the result.
    :param a: First number
    :param b: Second number
    :return: Sum of a and b
    """
    return a + b


if __name__ == "__main__":
    # Example usage:
    result = add_numbers(5, 7)
    print(f"The sum of 5 and 7 is {result}")